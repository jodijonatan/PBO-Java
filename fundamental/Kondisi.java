package fundamental;

public class Kondisi {
    public static void main(String[] args) {
        byte angka = 15;
        if (angka > 10) {
            System.out.println("angka lebih besar dari 10");
        } else if (angka == 10) {
            System.out.println("angka sama dengan 10");
        } else {
            System.out.println("angka lebih kecil dari 10");
        }
    }
}